#!/bin/bash 

# Loops : When you want to run something for some number of times continuously, then we use LOOPS 

# There are 2 variaties of loops : 
#     1) For Loop        ( When you know how many times to be executed, we use for loop )
#     2) While Loop      ( when you don't know how many time to be executed, we use this and whileloop only runs if the condition is true )

for var in a b c d ; do 
    echo "Value is $var" 
done 